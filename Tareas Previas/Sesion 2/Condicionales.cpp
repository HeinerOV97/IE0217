#include <iostream>
using namespace std;

int main() {
    
    /*Se declaran variables*/
    int number = 5;

    /**
    En este caso se mostrara en pantalla el mensaje dentro del if ya que 5 es mayor a cero, 
    en caso contrario el codigo seguiria con las instrucciones despues del if
    **/
    if (number > 0) {
        cout << "El numero declarado es mayor a 5";
    }

    return 0;

}