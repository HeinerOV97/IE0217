# Hay diferentes tipos de tuplas 

#Tupla vacia
my_tuple = ()
print(my_tuple)

#Tuplas de enteros
my_tuple = (1, 2, 3)
print(my_tuple)

#Tuplas con tipos de datos diferentes
my_tuple = (1, "Hello", 3.4)
print(my_tuple)

#Tuplas anidadas, se pueden almacenar listas y tuplas dentro de otra tupla
my_tuple = ("mouse", [8, 4, 6], (1, 2, 3))
print(my_tuple)