num1 = 1 # Variable tipo entero
num2 = 2 # Variable tipo entero
print(num1+num2) # Se imprime la suma de las variables, el interprete sabe que hacer si hay un + entre enteros

str1 = "Python" # Variable tipo string
str2 = "Programming" # Variable tipo string
print(str1+" "+str2) # Se concatenan los string, el interprete sabe que hacer si hay un + entre strings

# El interprete sabe como utilizar el comando len, dependiendo del tipo de dato
print(len("Programiz")) # Se imprime el tamano de un string
print(len(["Python", "Java", "C"])) # Se imprime el tamano de una lista
print(len({"Name": "John", "Address": "Nepal"})) # Se imprime el tamano de un diccionario